    class calculator {

        public add(num1: number, num2: number) {
            return num1 + num2;
        }

        public sub(num1: number, num2: number) {
            return num1 - num2;
        }

        public multiply(num1: number, num2: number) {
            return num1 * num2;
        }

        public divide(num1: number, num2: number) {
            return num1 / num2;
        }

    }
